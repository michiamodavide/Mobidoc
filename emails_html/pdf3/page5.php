  <h2>6. Diritti dell’interessato</h2>
  <!-------------------------------------------------------------------------------------------------------------------------------------------------------------->
  <p>I punti seguenti illustrano i diritti che potrà esercitare, in qualità di interessato, in relazione al trattamento dei Suoi dati personali:</p>
  <ul class="arrow">
    <li>I punti seguenti illustrano i diritti che potrà esercitare, in qualità di interessato, in relazione al trattamento dei Suoi dati personali: </li>
    <li>ha diritto, inoltre, di ottenere l’indicazione: a) dell’origine dei dati personali; b) delle finalità e modalità del trattamento; c) della logica applicata in caso di trattamento effettuato con l’ausilio di strumenti elettronici; d) degli estremi identificativi del Titolare, dei responsabili e del rappresentante designato ai sensi dell’articolo 5, comma secondo; e) dei soggetti o delle categorie di soggetti ai quali i dati personali possono essere comunicati o che possono venirne a conoscenza in qualità di rappresentante designato nel territorio dello Stato, di responsabili o autorizzati;</li>
    <li>ha diritto, altresì, di: a) ottenere l'aggiornamento, la rettificazione ovvero, quando vi ha interesse, l'integrazione dei dati; b) ottenere la cancellazione, la trasformazione in forma anonima o il blocco dei dati trattati in violazione di legge, compresi quelli di cui non è necessaria la conservazione in relazione agli scopi per i quali i dati sono stati raccolti o successivamente trattati; c) ottenere l'attestazione che le operazioni di cui alle lettere a) e b) sono state portate a conoscenza, anche per quanto riguarda il loro contenuto, di coloro ai quali i dati sono stati comunicati o diffusi, eccettuato il caso in cui tale adempimento si rivela impossibile o comporta un impiego di mezzi manifestamente sproporzionato rispetto al diritto tutelato; d) in presenza di condizioni specificamente previste dalle legge, di ricevere in un formato strutturato, di uso comune e leggibile da dispositivo automatico i dati personali che lo riguardano forniti a un Titolare del trattamento e ha il diritto di trasmettere tali dati a un altro Titolare del trattamento senza impedimenti da parte del Titolare del trattamento cui li ha forniti, anche mediante trasmissione diretta (cosiddetto diritto alla portabilità dei dati); e) proporre reclamo al Garante per la Protezione dei Dati Personali in Roma;</li>
    <li>ha infine diritto di opporsi, in tutto o in parte: a) per motivi legittimi al trattamento dei dati personali che lo riguardano, ancorché pertinenti allo scopo della raccolta; b) al trattamento di dati personali che lo riguardano a fini di invio di materiale pubblicitario o di vendita diretta o per il compimento di ricerche di mercato o di comunicazione commerciale.</li>
  </ul>
  <p>Tali diritti potranno essere esercitati rivolgendosi al Titolare di trattamento, scrivendo agli indirizzi sopra indicati.<br>
    <br>
    Il Titolare del trattamento si riserva il diritto di apportare modifiche alle presenti politiche di trattamento dei dati personali in qualunque momento, dandone comunicazione agli interessati e richiedendo eventuali consensi ulteriori che si rendano necessari, previa lettura dell’informativa
    aggiornata, prima di proseguire con il trattamento.</p>
  
  
